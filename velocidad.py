# Descripcion: Este programa calcula la distancia que recorre un auto en 6hrs y 3.5, tambien el tiempo que se tardaria en recorrer 485 km.


v = float(input("¿A que velocidad va el auto en Km/h?"))
d = v * 6
print("La distancia que recoore en 6hrs es: ", d, "Km")

d2 = v * 3.5
print ("La distancia que recoore en 3.5hrs es: ", d2, "Km")

t2 = 485 / v
print ("El tiempo que tarda en recorrer 485 Km es de: ", t2, "Horas")


